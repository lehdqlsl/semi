package com.team1.vo;

public class BoardUpVo {
	private int b_num;
	private int num;

	public BoardUpVo(int b_num, int num) {
		super();
		this.b_num = b_num;
		this.num = num;
	}

	public int getB_num() {
		return b_num;
	}

	public void setB_num(int b_num) {
		this.b_num = b_num;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

}
