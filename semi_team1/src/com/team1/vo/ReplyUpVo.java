package com.team1.vo;

public class ReplyUpVo {
	private int r_num;
	private int num;

	public ReplyUpVo(int r_num, int num) {
		super();
		this.r_num = r_num;
		this.num = num;
	}

	public int getR_num() {
		return r_num;
	}

	public void setR_num(int r_num) {
		this.r_num = r_num;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

}
